// Sample JavaScript program — basic arithmetic and control flow

let x = 10;
let y = 20;
let sum = x + y;

// Print the sum
console.log(sum);

/* Multi-line comment:
   This tests if/else */
if (sum > 25) {
    console.log("big");
} else {
    console.log("small");
}

// While loop
let i = 0;
while (i < 5) {
    console.log(i);
    i = i + 1;
}
